# 单位长度经纬度
unit_distance = 0.1
# 步数
step_num = 5
# 产生 rect 为水经注下载服务
def new_get_rect_list_by_lng_lat(center_lng, center_lat):
    rect_list = []
    start_point_lng = center_lng - step_num * unit_distance / 2
    start_point_lat = center_lat - step_num * unit_distance / 2
    for idx_in_lng in range(step_num):
        lower_left_lng = start_point_lng + idx_in_lng * unit_distance
        for idx_in_lat in range(step_num):
            lower_left_lat = start_point_lat + idx_in_lat * unit_distance
            tmp = '%.6f,%.6f|%.6f,%.6f'%(lower_left_lat, 
                                         lower_left_lng, 
                                         lower_left_lat + unit_distance, 
                                         lower_left_lng + unit_distance)
            rect_list.append(tmp)
    return rect_list

rect_list = new_get_rect_list_by_lng_lat(center_lat, center_lng)

